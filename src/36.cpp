#include <iostream>
using namespace std;

int main() {
    int a = 5;
    double b = 3.14;
    char c = 'A';
    long d = 0xabcdef; // 用 hexadecimal 编码

    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "c = " << c << endl;
    cout << "d = " << d << endl;

    return 0;
}
